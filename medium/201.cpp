/*
**	LeetCode: 201. [medium] Bitwise AND of Numbers Range
*/

class Solution {
	public:
		int rangeBitwiseAnd(int m, int n) {
			int res = m & n;

			for (uint32_t i = 1; m + i <= n; i = i * 2) {
				res &= m + i;
				if (res == 0)
					return res;
			}
			return res;
		}
};
