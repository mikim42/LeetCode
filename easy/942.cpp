/*
**	LeetCode: 942. DI String Match [easy]
*/

class Solution {
	public:
		vector<int> diStringMatch(string S) {
			int inc = 0, dec = S.size();
			vector<int> v(dec);

			for (int i = 0; i < S.size(); i++)
				v[i] = S[i] == 'I' ? inc++ : dec--;
			v.push_back(inc);
			return v;
		}
};
