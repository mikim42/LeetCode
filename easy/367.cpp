/*
**	LeetCode: 367. Valid Perfect Square [easy]
*/

class Solution {
	public:
		bool isPerfectSquare(int num) {
			int i = 1;

			while (i < 46341 && i * i < num) {
				i++;
			}
			return i < 46341 && i * i == num;
		}
};
