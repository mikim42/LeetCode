/*
**	LeetCode: 191. Number of 1 Bits [easy]
*/

class Solution {
	public:
		int hammingWeight(uint32_t n) {
			while (n > 0) {
				cnt += (1 & n);
				n >>= 1;
			}
			return cnt;
		}

	private:
		int cnt;
};
