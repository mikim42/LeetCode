/*
**	LeetCode: 804. Unique Morse Code Words [easy]
*/

class Solution {
	private:
		string MORSE[26] = {
			".-", "-...", "-.-.", "-..", ".", "..-.", "--.", "....", "..",
			".---", "-.-", ".-..", "--", "-.", "---", ".--.", "--.-", ".-.",
			"...", "-", "..-", "...-", ".--", "-..-", "-.--", "--.."
		};

	public:
		int uniqueMorseRepresentations(vector<string>& words) {
			unordered_map<string, int> m;

			for (auto &x : words) {
				string tmp = "";
				for (auto &y : x) {
					tmp += MORSE[y - 'a'];
				}
				m[tmp]++;
			}
			return m.size();
		}
};
