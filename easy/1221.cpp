/*
**	1221. Split a String in Balanced Strings [easy]
*/

class Solution {
	public:
		int balancedStringSplit(string s) {
			int cnt = 0, res = 0;

			for (int i = 0; i < s.size(); i++) {
				s[i] == 'R' ? cnt++ : cnt--;
				cnt == 0 ? res++ : 0;
			}
			return res;
		}
};
