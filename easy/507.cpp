/*
**	LeetCode: 507. Perfect Number [easy]
*/

class Solution {
	public:
		bool checkPerfectNumber(int num) {
			unordered_set<int> n = {6, 28, 496, 8128, 33550336};
			return n.count(num);
		}
};
