/*
**	LeetCode: 680. Valid Palindrome II [easy]
*/

class Solution {
	public:
		bool validPalindrome(string s) {
			int l = 0, r = s.size() - 1;
			while (l < r) {
				if (s[l] != s[r]) {
					int l1 = l, r1 = r;
					while (l + 1 < s.size() && r >= 0 && s[l + 1] == s[r])
						l++, r--;
					while (l1 < s.size() && r1 - 1 >= 0 && s[l1] == s[r1 - 1])
						l1++, r1--;
					return l + 1 >= r || l1 >= r1 - 1;
				}
				l++, r--;
			}
			return true;
		}
};
