/*
**	LeetCode: 461. Hamming Distance [easy]
*/

class Solution {
	public:
		int hammingDistance(int x, int y) {
			int res = 0;
			for (int mask = 0; mask < 32; mask++)
				res += (((x ^ y) & (1 << mask)) > 0);
			return res;
		}
};
