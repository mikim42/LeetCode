/*
**	LeetCode: 1018. Binary Prefix Divisible By 5 [easy]
*/

class Solution {
	public:
		vector<bool> prefixesDivBy5(vector<int>& A) {
			vector<bool> res(A.size(), false);
			int n = 0;

			for (int i = 0; i< A.size(); i++) {
				n = (n << 1 | A[i]) % 5;
				n == 0 ? res[i] = true : 0;
			}
			return res;
		}
};
