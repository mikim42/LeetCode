/*
**	LeetCode: 168. Excel Sheet Column Title [easy]
*/

class Solution {
	public:
		string convertToTitle(int n) {
			string res;

			while (n-- > 0) {
				res.push_back(char(n % 26 + 'A'));
				n /= 26;
			}
			reverse(res.begin(), res.end());
			return res;
		}
};
