/*
**	LeetCode: 876. Middle of the Linked List [easy]
*/

class Solution {
	public:
		ListNode* middleNode(ListNode* head) {
			ListNode *p1, *p2;

			p1 = head;
			p2 = head;
			while (p1) {
				p1 = p1->next;
				if (p1) {
					p1 = p1->next;
					p2 = p2->next;
				}
			}
			return p2;
		}
};
