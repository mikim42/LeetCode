/*
**	LeetCode: 832. Flipping an Image [easy]
*/

class Solution {
	public:
		vector<vector<int>> flipAndInvertImage(vector<vector<int>>& A) {
			for(size_t i = 0; i < A.size(); i++) {
				for (size_t j = 0; j < A[i].size() / 2; j++) {
					A[i][j] ^= 1;
					swap(A[i][j], A[i][A[i].size() - 1 - j]);
					A[i][j] ^= 1;
				}
				if (A[i].size() % 2 == 1)
					A[i][A[i].size() / 2] ^= 1;
			}
			return A;
		}
};
