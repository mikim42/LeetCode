/*
**	LeetCode: 598. Range Addition II [easy]
*/
class Solution {
	public:
		int maxCount(int m, int n, vector<vector<int>>& ops) {
			int h = m, w = n;

			for (auto x : ops) {
				h = min(h, x[0]);
				w = min(w, x[1]);
			}
			return w * h;
		}
};
