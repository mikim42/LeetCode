/*
**	LeetCode: 171. Excel Sheet Column Number [easy]
*/

class Solution {
	public:
		int titleToNumber(string s) {
			int n = 0;

			for (int i = 0; i < s.size(); i++) {
				n *= 26;
				n += s[i] - ('A' - 1);
			}
			return n;
		}
};
