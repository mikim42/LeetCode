/*
**	LeetCode: 557. Reverse Words in a String III [easy]
*/

class Solution {
	public:
		string reverseWords(string s) {
			for (int i = 0, p = 0; i < s.size(); i++) {
				if (i == s.size() - 1 || s[i + 1] == ' ') {
					for (int j = 0; p + j <= (p + i) / 2; j++)
						swap(s[p + j], s[i - j]);
					p = i + 2;
				}
			}
			return s;
		}
};
