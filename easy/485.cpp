/*
**	LeetCode: 485. Max Consecutive Ones [easy]
*/

class Solution {
	public:
		int findMaxConsecutiveOnes(vector<int>& nums) {
			int r = 0, cnt = 0;

			for (auto x : nums) {
				if (x == 1)
					cnt++;
				else {
					r = max(r, cnt);
					cnt = 0;
				}
			}
			r = max(r, cnt);
			return r;
		}
};
