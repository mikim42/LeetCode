/*
**	LeetCode: 405. Convert a Number to Hexadecimal [easy]
*/

class Solution {
	public:
		string toHex(int num) {
			char chars[] = "0123456789abcdef";
			string hex = "";

			while (num > 0) {
				hex.insert(hex.begin(), chars[num % 16]);
				num /= 16;
			}
			return hex;
		}
};
