/*
**	LeetCode:  70. Climbing Stairs [easy]
*/

class Solution {
	public:
		int climbStairs(int n) {
			int prev1 = 1, prev2 = 1;
			int res = 3;

			if (n < 4)
				return n;
			while (n > 3) {
				int tmp = prev1 + prev2;
				res += tmp;
				prev1 = prev2;
				prev2 = tmp;
				n--;
			}
			return res;
		}
};
